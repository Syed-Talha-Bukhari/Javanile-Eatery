package restaurant;

public class Cart {
    public String item;
    public String qty;

    public Cart(String item, String qty, String price) {
        this.item = item;
        this.qty = qty;
        this.price = price;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
    public String price;
    
    
}
